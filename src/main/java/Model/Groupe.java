package Model;

public class Groupe {
    protected  Integer codeG;
    protected  String nomG;

    public Groupe() {
    }

    public Integer getCodeG() {
        return codeG;
    }

    public void setCodeG(Integer codeG) {
        this.codeG = codeG;
    }

    public String getNomG() {
        return nomG;
    }

    public void setNomG(String nomG) {
        this.nomG = nomG;
    }
}
